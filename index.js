function saludar() {
    respuesta = prompt("¡Hola Mundo! ¿Como estan?");
    if (respuesta == "muy bien") {
        alert("Exelente")
    } else if (respuesta == "bien") {
        alert("Me alegro")
    } else {
        alert("Que mal")
    }
    console.log(saludar);
}
saludar()

